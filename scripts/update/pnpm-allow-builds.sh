#!/usr/bin/env bash

set -euo pipefail

export PS4=$'\n+CMD [\D{%Y-%m-%dT%H:%M:%S%z}] [${BASH_SOURCE##*/}:${LINENO}] '

set -x

# cSpell: ignore inplace strenv

repo_root="${REPO_ROOT:-${HOME}/repos}"
repo_names=(
  hub
  node-app
  react-app
  vault
)

commit_message="chore: refresh pnpm allow builds"
timestamp="$(date -u +%Y%m%d%H%M%S)"

for required_command in fnm gh git jq pnpm yq; do
  command -v "${required_command}" >/dev/null
done

gh auth status

for repo_name in "${repo_names[@]}"; do
  repo_path="${repo_root}/${repo_name}"

  (
    cd "${repo_path}"

    if [[ -n "$(git status --porcelain)" ]]; then
      echo "Repository has uncommitted changes: ${repo_path}" >&2
      exit 1
    fi

    git switch main
    git pull --ff-only --prune origin main

    fnm use

    open_pr_url="$(
      gh pr list --state open --json title,url |
        jq --raw-output --arg title "${commit_message}" \
          'first(.[] | select(.title == $title) | .url) // empty'
    )"

    if [[ -n "${open_pr_url}" ]]; then
      echo "Open pull request already exists: ${open_pr_url}"
      exit 0
    fi

    branch_name="chore/refresh-pnpm-allow-builds-${timestamp}"

    git switch --create "${branch_name}"

    allow_builds="$(
      yq eval --unwrapScalar '.allowBuilds // {} | keys | .[]' pnpm-workspace.yaml
    )"

    while IFS= read -r package_name; do
      if [[ -z "${package_name}" ]]; then
        continue
      fi

      if [[ ! "${package_name}" =~ ^[a-zA-Z0-9][a-zA-Z0-9._-]*$ ]] &&
        [[ ! "${package_name}" =~ ^@[a-zA-Z0-9][a-zA-Z0-9._-]*/[a-zA-Z0-9][a-zA-Z0-9._-]*$ ]]; then
        echo "Skipping non-package allowBuilds matcher: ${package_name}"
        continue
      fi

      dependent_count="$(pnpm why --json "${package_name}" | jq 'length')"

      if ((dependent_count == 0)); then
        PACKAGE_NAME="${package_name}" yq --inplace \
          'del(.allowBuilds[strenv(PACKAGE_NAME)]) | del(.allowBuilds | select(length == 0))' \
          pnpm-workspace.yaml
      fi
    done <<<"${allow_builds}"

    rm -rf node_modules
    pnpm install --frozen-lockfile --config.strictDepBuilds=false
    pnpm approve-builds --all

    unexpected_changes="$(
      git status --porcelain |
        sed -E 's/^.. //' |
        grep -v '^pnpm-workspace.yaml$' || true
    )"

    if [[ -n "${unexpected_changes}" ]]; then
      echo "Unexpected repository changes:" >&2
      echo "${unexpected_changes}" >&2
      exit 1
    fi

    if git diff --quiet -- pnpm-workspace.yaml; then
      echo "No pnpm build approval changes: ${repo_name}"
      git switch main
      git branch --delete "${branch_name}"
      exit 0
    fi

    git add pnpm-workspace.yaml
    git commit --signoff --message "${commit_message}"
    git push --set-upstream origin "${branch_name}"

    pr_url="$(
      gh pr create \
        --base main \
        --head "${branch_name}" \
        --draft \
        --title "${commit_message}" \
        --body "Refresh pnpm build approvals against the current locked dependency graph. Remove stale package entries, preserve existing decisions for remaining locked dependencies, and approve newly pending builds. Generated by \`scripts/update/pnpm-allow-builds.sh\`."
    )"

    echo "Created pull request: ${pr_url}"

    git switch main
  )
done
